package ar.com.practicas;

public class OperadoresAsignacion {

    public static void main(String[] args) {

        //Asignar Valores
        int varA = 23, varB=12;


        int varC = varA + 5 - varB;
        System.out.println("varC = " + varC);

        //Aginacion +=   -->  //a = a + 1
        varA += 1;
        System.out.println("varA = " + varA);

        //Aginacion +=   -->  //a = a + 3
        varA += 3;
        System.out.println("varA = " + varA);

        //Aginacion -=   -->  //a = a - 2
        varA -= 2;
        System.out.println("varA = " + varA);

        //Otros

        // *=   /=   %=
    }
}
